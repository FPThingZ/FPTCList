export async function fetchUnratedLeaderboard() {
    const list = await fetchUnrated();

    const scoreMap = {};
    const errs = [];
    list.forEach(([level, err], rank) => {
        if (err) {
            errs.push(err);
            return;
        }

        const verifier = Object.keys(scoreMap).find(
            (u) => u.toLowerCase() === level.verifier.toLowerCase(),
        ) || level.verifier;
        scoreMap[verifier] ??= { verified: [], completed: [], progressed: [] };
        scoreMap[verifier].verified.push({
            rank: rank + 1,
            level: level.name,
            score: score(rank + 1, 100, level.percentToQualify),
            link: level.verification,
        });

        level.records.forEach((record) => {
            const user = Object.keys(scoreMap).find(
                (u) => u.toLowerCase() === record.user.toLowerCase(),
            ) || record.user;
            scoreMap[user] ??= { verified: [], completed: [], progressed: [] };
            const { completed, progressed } = scoreMap[user];
            if (record.percent === 100) {
                completed.push({
                    rank: rank + 1,
                    level: level.name,
                    score: score(rank + 1, 100, level.percentToQualify),
                    link: record.link,
                });
            } else {
                progressed.push({
                    rank: rank + 1,
                    level: level.name,
                    percent: record.percent,
                    score: score(rank + 1, record.percent, level.percentToQualify),
                    link: record.link,
                });
            }
        });
    });

    const res = Object.entries(scoreMap).map(([user, scores]) => {
        const { verified, completed, progressed } = scores;
        const total = [verified, completed, progressed]
            .flat()
            .reduce((prev, cur) => prev + cur.score, 0);
        return { user, total: round(total), ...scores };
    });

    return [res.sort((a, b) => b.total - a.total), errs];
}

export async function fetchCombinedLeaderboard() {
    const [[ratedBoard, ratedErrs], [unratedBoard, unratedErrs]] =
        await Promise.all([fetchLeaderboard(), fetchUnratedLeaderboard()]);

    const combinedMap = {};

    for (const entry of ratedBoard) {
        combinedMap[entry.user] ??= { verified: [], completed: [], progressed: [], ratedTotal: 0, unratedTotal: 0 };
        combinedMap[entry.user].verified.push(...entry.verified);
        combinedMap[entry.user].completed.push(...entry.completed);
        combinedMap[entry.user].progressed.push(...entry.progressed);
        combinedMap[entry.user].ratedTotal = entry.total;
    }

    for (const entry of unratedBoard) {
        const key = Object.keys(combinedMap).find(
            (u) => u.toLowerCase() === entry.user.toLowerCase(),
        ) || entry.user;
        combinedMap[key] ??= { verified: [], completed: [], progressed: [], ratedTotal: 0, unratedTotal: 0 };
        combinedMap[key].verified.push(...entry.verified);
        combinedMap[key].completed.push(...entry.completed);
        combinedMap[key].progressed.push(...entry.progressed);
        combinedMap[key].unratedTotal = entry.total;
    }

    const res = Object.entries(combinedMap).map(([user, data]) => ({
        user,
        total: round(data.ratedTotal + data.unratedTotal),
        verified: data.verified,
        completed: data.completed,
        progressed: data.progressed,
    }));

    return [res.sort((a, b) => b.total - a.total), [...ratedErrs, ...unratedErrs]];
}

